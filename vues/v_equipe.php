            <h6>La boite à comptines, c'est quoi ?</h6>
            <p>Vous cherchez les paroles et les gestes d’une  comptine de votre enfance ? Vous souhaitez apprendre de nouvelles comptines ? Ou encore, vous connaissez de superbes comptines et désirez les partager ? Alors, la boîte à comptines est faite pour vous !</p>
            <p>La boîte à comptines est une application collaborative de collecte et de partage des comptines. Elle est disponible sur internet et téléchargeable en tant qu’application sur iphone et android.</p>
            <p>Les comptines sont de petites chansons faciles à retenir accompagnées de gestes. Elles sont au cœur de l’apprentissage culturel et linguistique des tout-petits.  Notre ambition est de rassembler, grâce à la participation des utilisateurs, le plus grand nombre des comptines en vidéo pour les rendre accessibles à tous. Nous espérons aider ainsi les parents et les professionnels dans l’éducation des enfants.</p>
            <p>La boîte à comptines est un projet de parent-nounou.fr [lien]. Elle est née lors du week-end Open E-Education (22-24 novembre 2013). Une équipe de plusieurs développeurs et graphistes passionnés par l’éducation des tout-petits s’est réunie autour d’Anna Stépanoff, porteur du projet, pour lui donner des ailes.</p>
            <p>L’équipe : Charlotte Breton, Romain Cœur, Louis Courau, Nguyen Dang, Emmanuelle Gousse, Mehdi Guiraud, Benjamin Heinen, Montgomery Mgomri, Leila Mousstamine, Geoffrey Nguyen, Gregory Peigne, Clémentine Stépanoff.</p>
